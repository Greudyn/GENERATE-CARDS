create or replace PROCEDURE             "GENERAL_GET" (TABLE_NAME IN OUT VARCHAR2, TABLE_DATA OUT SYS_REFCURSOR, TABLE_PROPERTIE VARCHAR2, COLUMN_WHERE VARCHAR2, COLUMN_VALUES VARCHAR2) AS 
BEGIN

IF COLUMN_WHERE IS NULL THEN
    OPEN    TABLE_DATA
    FOR
        'SELECT  '||TABLE_PROPERTIE||'
        FROM    '||TABLE_NAME
			;


    TABLE_NAME:=1;        
    COMMIT;
ELSE
    OPEN    TABLE_DATA
    FOR
        'SELECT  '||TABLE_PROPERTIE||'
        FROM    '||TABLE_NAME||'
        WHERE '||COLUMN_WHERE||' IN '||COLUMN_VALUES
			;

    TABLE_NAME:=1;        
    COMMIT;

END IF;
EXCEPTION
	WHEN OTHERS THEN

        TABLE_NAME:=0;
		dbms_output.put_line('ENTRE EN ERRORES ==> ERROR :'||SQLCODE||' -ERROR- '||SQLERRM);
		ROLLBACK;

END GENERAL_GET;

